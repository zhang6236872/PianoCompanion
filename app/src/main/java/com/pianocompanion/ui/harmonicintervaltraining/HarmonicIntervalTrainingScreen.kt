            Text("4. 从音程选项中选出正确的答案", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Spacer(modifier = Modifier.height(4.dp))
            Text("🎼 和声音程指南", style = MaterialTheme.typography.titleSmall, fontWeight = FontWeight.Bold)
            Text("• 🌑 小三度（m3）— 3 半音，柔和暗淡，小调色彩", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• ☀️ 大三度（M3）— 4 半音，明亮温暖，大调色彩", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• 🌊 纯四度（P4）— 5 半音，悬浮开阔", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
