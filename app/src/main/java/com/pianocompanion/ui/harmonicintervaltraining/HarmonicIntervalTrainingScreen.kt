/**
 * 和声音程辨识训练主界面（Material 3 Compose）。
 *
 * 功能流程（和声音程辨识训练）：
 * 1. 选择难度（初级/中级/高级）
 * 2. 开始练习后，播放两个音同时响起的和声音程
 * 3. 用户根据和声色彩判断音程类型（协和/不协和实践感）
 * 4. 从选项中选出正确答案
 * 5. 答题后显示对错 + 音程描述，点击「下一题」继续
 *
 * 核心训练：辨识两个音同时演奏时产生的和声色彩（音程的"浓度"与协和度）。
 */
@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun HarmonicIntervalTrainingScreen(
    viewModel: HarmonicIntervalViewModel = run {
        val context = androidx.compose.ui.platform.LocalContext.current
        viewModel(factory = object : androidx.lifecycle.ViewModelProvider.Factory {
            override fun <T : androidx.lifecycle.ViewModel> create(modelClass: Class<T>): T {
                @Suppress("UNCHECKED_CAST")
                return HarmonicIntervalViewModel(context.applicationContext as android.app.Application) as T
            }
        })
    }
) {
    val uiState by viewModel.uiState.collectAsState()

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("🎵 和声音程辨识", fontWeight = FontWeight.Bold) },
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.primaryContainer
                )
            )
        }
    ) { padding ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(padding)
                .verticalScroll(rememberScrollState())
                .padding(horizontal = 16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            if (!uiState.isSessionActive) {
                HarmonicIntervalSetupPanel(
                    difficulty = uiState.difficulty,
                    progress = uiState.progress,
                    onStart = { viewModel.startSession(uiState.difficulty) }
                )
            } else {
                HarmonicIntervalPracticePanel(
                    uiState = uiState,
                    onPlayAudio = { viewModel.playAudio() },
                    onStopAudio = { viewModel.stopAudio() },
                    onSubmit = { viewModel.submitAnswer(it) },
                    onNext = { viewModel.nextQuestion() },
                    onEnd = { viewModel.endSession() }
                )
            }
            Spacer(modifier = Modifier.height(16.dp))
        }
    }
}

