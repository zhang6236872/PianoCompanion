@Composable
private fun TempoTrainingSetupPanel(
    difficulty: TempoTrainingDifficulty,
    progress: TempoTrainingProgress,
    onStart: () -> Unit
) {
    var selectedDifficulty by remember { mutableStateOf(difficulty) }

    // 难度选择
    Card(
        modifier = Modifier.fillMaxWidth(),
        colors = CardDefaults.cardColors(containerColor = MaterialTheme.colorScheme.surfaceVariant),
        shape = RoundedCornerShape(14.dp)
    ) {
        Column(modifier = Modifier.padding(16.dp), verticalArrangement = Arrangement.spacedBy(8.dp)) {
            Text("选择难度", style = MaterialTheme.typography.titleSmall, fontWeight = FontWeight.Bold)
            Column(verticalArrangement = Arrangement.spacedBy(6.dp)) {
                TempoTrainingDifficulty.ALL.forEach { d ->
                    FilterChip(
                        selected = selectedDifficulty == d,
                        onClick = { selectedDifficulty = d },
                        label = { Text("${d.displayName}（${d.description}）") }
                    )
                }
            }
        }
    }

    // 进度展示
    if (progress.totalAnswered > 0) {
        Card(
            modifier = Modifier.fillMaxWidth(),
            colors = CardDefaults.cardColors(containerColor = MaterialTheme.colorScheme.primaryContainer),
            shape = RoundedCornerShape(14.dp)
        ) {
            Column(modifier = Modifier.padding(16.dp), verticalArrangement = Arrangement.spacedBy(6.dp)) {
                Text("📊 练习记录", style = MaterialTheme.typography.titleSmall, fontWeight = FontWeight.Bold)
                Row(modifier = Modifier.fillMaxWidth(), horizontalArrangement = Arrangement.SpaceBetween) {
                    TTStatColumn("总答题", "${progress.totalAnswered}")
                    TTStatColumn("正确", "${progress.totalCorrect}")
                    TTStatColumn("准确率", "${"%.0f".format(progress.overallAccuracy * 100)}%")
                    TTStatColumn("最长连击", "${progress.overallBestStreak}")
                }
            }
        }
    }

    // 开始按钮
    Button(
        onClick = onStart,
        modifier = Modifier
            .fillMaxWidth()
            .height(52.dp),
        shape = RoundedCornerShape(14.dp)
    ) {
        Icon(Icons.Filled.PlayArrow, "开始")
        Spacer(modifier = Modifier.width(8.dp))
        Text("开始听辨", fontSize = 18.sp, fontWeight = FontWeight.Bold)
    }

    // 说明卡片
    Card(
        modifier = Modifier.fillMaxWidth(),
        colors = CardDefaults.cardColors(containerColor = MaterialTheme.colorScheme.surface),
        shape = RoundedCornerShape(14.dp)
    ) {
        Column(modifier = Modifier.padding(16.dp), verticalArrangement = Arrangement.spacedBy(4.dp)) {
            Text("💡 如何练习", style = MaterialTheme.typography.titleSmall, fontWeight = FontWeight.Bold)
            Text("1. 点击播放，听一段匀速节拍（8 个「哒」声）", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("2. 感受节拍的速度——是慢悠悠的、悠闲的、适中的、还是急促的？", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("3. 从速度术语选项中选出与节拍速度最匹配的答案", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("4. 可以反复播放，直到确定速度感", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Spacer(modifier = Modifier.height(4.dp))
            Text("🎼 意大利语速度术语", style = MaterialTheme.typography.titleSmall, fontWeight = FontWeight.Bold)
            Text("• Largo（广板）~50 BPM — 缓慢庄严，拍间间隙很大", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• Adagio（柔板）~70 BPM — 从容舒展", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• Andante（行板）~90 BPM — 步行速度，轻柔自然", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• Moderato（中板）~120 BPM — 不快不慢，练习首选", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• Allegro（快板）~140 BPM — 快速活跃，充满活力", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• Presto（急板）~180 BPM — 极快急促，炫技速度", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Spacer(modifier = Modifier.height(4.dp))
            Text("🔑 听辨技巧", style = MaterialTheme.typography.titleSmall, fontWeight = FontWeight.Bold)
            Text("• 试着跟着打拍子（跺脚/点头），感受内在的节拍脉冲", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
            Text("• 120 BPM = 每秒 2 拍 = 秒针走动的速度，以此作为锚点", fontSize = 13.sp, color = MaterialTheme.colorScheme.onSurfaceVariant)
